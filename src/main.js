document.querySelectorAll('#page-console .card').forEach(card => {
  card.addEventListener('click', () => {
    if (busy) return;
    const console = card.dataset.console;
    const changed = selectedConsole !== console;
    selectedConsole = console;

    document.querySelectorAll('#page-console .card').forEach(c => c.classList.remove('active'));
    card.classList.add('active');

    if (changed) {
      imagePath = null;
      $('image-name').textContent = t('no_image');
      $('image-version').textContent = '';
      // Reset overlay pick when console changes (different variants
      // expect different DTBs and the user's previous selection might
      // no longer be appropriate).
      flashOverlayPath = '';
      flashOverlaySkip = false;
      const pickedLabel = $('overlay-picked-flash');
      if (pickedLabel) pickedLabel.textContent = t('no_overlay_picked');
      const skipBox = $('overlay-skip-flash');
      if (skipBox) skipBox.checked = false;
      checkLatestVersion(console);
    }

    updateNextButton();
  });
});

