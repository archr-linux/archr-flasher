async function pickOverlayDtbo() {
  try {
    const selected = await window.__TAURI__.dialog.open({
      filters: [{ name: 'Overlay (mipi-panel.dtbo)', extensions: ['dtbo'] }],
      multiple: false,
    });
    if (!selected) return null;
    return typeof selected === 'string' ? selected : (selected.path || selected);
  } catch (e) {
    setFlashStatus(t('error') + ': ' + e, 'error');
    return null;
  }
}

